var birthday = new Date(2000, 07, 18, 12, 00, 15);

//Gets the month of the Date (0 - 11)
console.log(birthday.getMonth());

//Gets the year of the Date (YYYY)
console.log(birthday.getFullYear());

//Gets the date of the month (1 - 31)
console.log(birthday.getDate());

//Gets day of the week (0 - 6)
console.log(birthday.getDay());

//Gets the hour of the day (0 - 21)
console.log(birthday.getHours());

//Gets the number of milliseconds since 18th August 2018
console.log(birthday.getTime());