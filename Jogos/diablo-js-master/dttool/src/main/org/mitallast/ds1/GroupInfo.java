package org.mitallast.ds1;

public class GroupInfo {
    int tile_x;
    int tile_y;
    int width;
    int height;
    int unk;

    @Override
    public String toString() {
        return "GroupInfo{" +
                "tile_x=" + tile_x +
                ", tile_y=" + tile_y +
                ", width=" + width +
                ", height=" + height +
                ", unk=" + unk +
                '}';
    }
}
